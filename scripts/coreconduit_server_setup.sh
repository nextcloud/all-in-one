# ── System hardening extras ───────────────────────────────────────────────────
system_hardening() {
  section "Additional System Hardening"

  # Disable core dumps
  cat >> /etc/security/limits.conf <<'EOF'
* hard core 0
EOF

  # Kernel hardening via sysctl
  cat > /etc/sysctl.d/99-coreconduit-hardening.conf <<'EOF'
# CoreConduit Kernel Hardening

# Prevent IP spoofing
net.ipv4.conf.all.rp_filter = 1
net.ipv4.conf.default.rp_filter = 1

# Ignore ICMP broadcast requests
net.ipv4.icmp_echo_ignore_broadcasts = 1

# Disable source packet routing
net.ipv4.conf.all.accept_source_route = 0
net.ipv6.conf.all.accept_source_route = 0

# Ignore send redirects
net.ipv4.conf.all.send_redirects = 0

# Block SYN attacks
net.ipv4.tcp_syncookies = 1
net.ipv4.tcp_max_syn_backlog = 2048
net.ipv4.tcp_synack_retries = 2
net.ipv4.tcp_syn_retries = 5

# Log spoofed, source-routed, and redirect packets
net.ipv4.conf.all.log_martians = 1

# Disable IPv6 if not needed (comment out to keep IPv6)
net.ipv6.conf.all.disable_ipv6 = 1
net.ipv6.conf.default.disable_ipv6 = 1
EOF

  sysctl --system 2>&1 | tee -a "$LOG_FILE"
  success "Kernel hardening parameters applied."
}

# ── Verification summary ──────────────────────────────────────────────────────
verify_setup() {
  section "Verification Summary"

  echo -e "\n${BOLD}── Services ─────────────────────────────────────────────${RESET}"
  for svc in ssh fail2ban ufw ollama; do
    status=$(systemctl is-active "$svc" 2>/dev/null || echo "inactive")
    if [[ "$status" == "active" ]]; then
      echo -e "  ${GREEN}●${RESET} $svc"
    else
      echo -e "  ${RED}●${RESET} $svc ($status)"
    fi
  done

  echo -e "\n${BOLD}── UFW Status ───────────────────────────────────────────${RESET}"
  ufw status numbered

  echo -e "\n${BOLD}── Ollama API ───────────────────────────────────────────${RESET}"
  if curl -sf "http://localhost:${OLLAMA_PORT}/api/tags" > /dev/null 2>&1; then
    echo -e "  ${GREEN}●${RESET} Ollama API responding at localhost:${OLLAMA_PORT}"
  else
    echo -e "  ${YELLOW}●${RESET} Ollama API not yet responding (may need reboot for GPU)"
  fi

  echo -e "\n${BOLD}── NVIDIA GPU ───────────────────────────────────────────${RESET}"
  if command -v nvidia-smi &>/dev/null; then
    nvidia-smi --query-gpu=name,driver_version,memory.total --format=csv,noheader 2>/dev/null \
      && success "nvidia-smi OK" \
      || warn "nvidia-smi present but GPU may not be loaded yet — reboot required."
  else
    warn "nvidia-smi not found — reboot may be needed to load drivers."
  fi

  echo ""
}

