# ── Main ──────────────────────────────────────────────────────────────────────
main() {
  REBOOT_REQUIRED=false
  exec > >(tee -a "$LOG_FILE") 2>&1

  echo -e "\n${BOLD}${CYAN}"
  echo "  ╔══════════════════════════════════════════════════════╗"
  echo "  ║   CoreConduit — Ollama LLM Server Setup v1.0.0      ║"
  echo "  ║   Ubuntu 24.04 · RTX 5070 · $(date '+%Y-%m-%d %H:%M')          ║"
  echo "  ╚══════════════════════════════════════════════════════╝"
  echo -e "${RESET}"

  validate_config
  system_update
  harden_ssh
  configure_fail2ban
  system_hardening
  configure_ufw
  install_nvidia
  install_ollama
  configure_motd
  verify_setup
  print_notes

  if [[ "$REBOOT_REQUIRED" == true ]]; then
    echo -e "${YELLOW}[REBOOT REQUIRED]${RESET} Run: ${CYAN}sudo reboot${RESET}\n"
  fi
}

main "$@"
